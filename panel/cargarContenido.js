function cargarImagen(){
            
                            if (document.getElementById("editarImagen").style.visibility=="hidden"){ 
                            document.getElementById("editarImagen").style.visibility="visible";}
                        else document.getElementById("editarImagen").style.visibility="hidden";
    }
    
